#include <stdio.h>

/**
 * add - add two inger numbers
 * @a: the firt parameter
 * @b: the second parameter
 * Return: the sum of the two numbers
 */
int add(int a, int b)
{
return (a + b);
}

/**
 * sub - subtract two given numbers
 * @a: the first parameter
 * @b: second parameter
 * Return: the subraction of the both numbers
 */
int sub(int a, int b)
{
return (a - b);
}

/**
 * div - divide two integer numbers
 * @a: the first parameter
 * @b: the second parameter
 * Return: the remainer of the numbers
 */
int div(int a, int b)
{
return (a / b);
}

/**
 * mul - mutiplies two integer numbers
 * @a: the first parameter
 * @b: the second parameter
 * Return: the multiplication of the given numbers
 */

int mul(int a, int b)
{
return (a * b);
}

/**
 * mod - return the modulus of two numbers
 * @a: first parameter
 * @b: the second parameter
 * Return: either true or false
 */

int mod(int a, int b)
{
return (a % b);
}
