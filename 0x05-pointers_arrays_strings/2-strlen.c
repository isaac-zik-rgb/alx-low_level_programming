/**
 * _strlen - function that returns character length
 *@s: a char data type is passed as a parameter
 *
 * Return: The length of a character string
 */
int _strlen(char *s)
{
  int count;
  s = count;
  for (count = 0; count != '\0'; count++)
    return count;
}
  
